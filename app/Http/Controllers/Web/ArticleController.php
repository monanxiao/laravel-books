    {
        /**
         * 加入队列记录 5秒后执行记录访问书籍章节
         */
        $result = ['article' => $article , 'server' => $request->server()];

        BooksChapterLook::dispatch($result)
                    ->delay(now()->now()->addSeconds(5))
                    ->onQueue('bookChapterook');

        return view('web.books.content', compact('article'));
    }

